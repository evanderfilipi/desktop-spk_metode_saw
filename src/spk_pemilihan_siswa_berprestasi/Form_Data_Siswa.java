    private void simpanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBtnActionPerformed
        // TODO add your handling code here:
        String nisnya = "";
        String namanya = "";
        
        if (jeniskelaminCmb.getSelectedIndex()==0){ 
        jns_kelamin="";}
        else if(jeniskelaminCmb.getSelectedIndex()==1){
        jns_kelamin="Laki-laki";}
        else if(jeniskelaminCmb.getSelectedIndex()==2){
        jns_kelamin="Perempuan";}
        
        if (agamaCmb.getSelectedIndex()==0){ 
        agama="";}
        else if(agamaCmb.getSelectedIndex()==1){
        agama="Islam";}
        else if(agamaCmb.getSelectedIndex()==2){
        agama="Kristen";}
        else if(agamaCmb.getSelectedIndex()==3){
        agama="Katholik";}
        else if(agamaCmb.getSelectedIndex()==4){
        agama="Buddha";}
        else if(agamaCmb.getSelectedIndex()==5){
        agama="Hindu";}
        else if(agamaCmb.getSelectedIndex()==6){
        agama="Konghucu";}
        if (nisTxt.getText().equals("") || namaTxt.getText().equals("") || tempatlahirTxt.getText().equals("") || tgl_lahir.equals("") || jns_kelamin.equals("") || agama.equals("") || alamatTxt.getText().equals("") || walimuridTxt.getText().equals("") || walimurid2Txt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon lengkapi data terlebih dahulu!","Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
        } else {
        try {
           java.sql.Connection conn = (java.sql.Connection)Koneksi.getConnection();
           java.sql.Statement stm = conn.createStatement();
           java.sql.ResultSet sqll = stm.executeQuery("SELECT NIS, Nama FROM tb_datasiswa WHERE NIS='"+nisTxt.getText()+"' AND Nama='"+namaTxt.getText()+"'");        
           if (sqll.next()) {
               nis = sqll.getInt("NIS");
               nisnya = Integer.toString(nis);
               namanya = sqll.getString("Nama");
           } else {}
           if (nisTxt.getText().equals(nisnya)){
           JOptionPane.showMessageDialog(null, "Data siswa dengan NIS "+nisTxt.getText()+" sudah ada, dengan nama "+namanya+"!","Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);    
           } else {
                String sql = "INSERT INTO tb_datasiswa VALUES('"+nisTxt.getText()+"','"+namaTxt.getText()+"','"+tempatlahirTxt.getText()+"','"+tgl_lahir+"','"+jns_kelamin+"','"+agama+"','"+alamatTxt.getText()+"','"+notelpTxt.getText()+"','"+emailTxt.getText()+"',"
                        + "'"+walimuridTxt.getText()+"','"+walimurid2Txt.getText()+"','"+kd_admin+"')";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data siswa berhasil disimpan!","Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
        }
        } catch (SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, "Data siswa gagal disimpan!","Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
        }
        }
        getData();
    }//GEN-LAST:event_simpanBtnActionPerformed

