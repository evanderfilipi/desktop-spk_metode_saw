    private void datasiswaTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_datasiswaTblMouseClicked
        // TODO add your handling code here:
        try {
            int row = datasiswaTbl.getSelectedRow();
            String kliktabel=(datasiswaTbl.getModel().getValueAt(row,0).toString());
            java.sql.Connection conn = (java.sql.Connection)Koneksi.getConnection();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("SELECT * FROM tb_datasiswa WHERE NIS='"+kliktabel+"'");

            if (sql.next()) {
                nis = sql.getInt("NIS");
                nisString = Integer.toString(nis);
                nisTxt.setText(nisString);
                String nama = sql.getString("Nama");
                namaTxt.setText(nama);
                String tmplahir = sql.getString("Tmp_Lahir");
                tempatlahirTxt.setText(tmplahir);
                jDateChooser1.setDate(sql.getDate("Tgl_Lahir"));
                String jeniskelamin = sql.getString("Jenis_Kelamin");
                if(jeniskelamin.equals("")){
                    jeniskelaminCmb.setSelectedIndex(0);}
                else if(jeniskelamin.equals("Laki-laki")){
                    jeniskelaminCmb.setSelectedIndex(1);}
                else if(jeniskelamin.equals("Perempuan")){
                    jeniskelaminCmb.setSelectedIndex(2);}
                String agama = sql.getString("Agama");
                if(agama.equals("")){
                    agamaCmb.setSelectedIndex(0);}
                else if(agama.equals("Islam")){
                    agamaCmb.setSelectedIndex(1);}
                else if(agama.equals("Kristen")){
                    agamaCmb.setSelectedIndex(2);}
                else if(agama.equals("Katholik")){
                    agamaCmb.setSelectedIndex(3);}
                else if(agama.equals("Buddha")){
                    agamaCmb.setSelectedIndex(4);}
                else if(agama.equals("Hindu")){
                    agamaCmb.setSelectedIndex(5);}
                else if(agama.equals("Konghucu")){
                    agamaCmb.setSelectedIndex(6);}
                String alamat = sql.getString("Alamat");
                alamatTxt.setText(alamat);
                String notelp = sql.getString("No_Telepon");
                notelpTxt.setText(notelp);
                String email = sql.getString("Email");
                emailTxt.setText(email);
                String walimurid = sql.getString("Nama_Ayah");
                walimuridTxt.setText(walimurid);
                String walimurid2 = sql.getString("Nama_Ibu");
                walimurid2Txt.setText(walimurid2);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tidak dapat terhubung ke database. Pastikan Xampp sudah aktif!","Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_datasiswaTblMouseClicked

